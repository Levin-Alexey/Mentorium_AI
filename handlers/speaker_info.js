export async function handleSpeakerInfo({ callbackQuery, env, telegram }) {
  const chatId = callbackQuery.message?.chat?.id;
  if (!chatId) {
    return;
  }

  await telegram.sendPhoto(env.BOT_TOKEN, {
    chat_id: chatId,
    photo: SPEAKER_PHOTO,
  });

  await telegram.sendMessage(env.BOT_TOKEN, {
    chat_id: chatId,
    text: SPEAKER_TEXT,
  });
}
